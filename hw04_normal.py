def textin(txt):
    lowreg = list(map(chr, range(ord('a'), ord('z') + 1)))
    higtreg = list(map(chr, range(ord('A'), ord('Z') + 1)))
    result = []
    prev_ch = ''
    word = ''
    for ch in txt:
        if ch in lowreg and (prev_ch in higtreg or prev_ch == '' or len(word) > 0):
            word += ch
        elif ch in higtreg and len(word) > 0:
            result.append(word)
            word = ''
        prev_ch = ch
    if len(word) > 0:
        result.append(word)
    return result

